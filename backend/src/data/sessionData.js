function getSessionsList() {
  const speakersById = new Map(eventSpeakers.map((speaker) => [speaker.id, speaker]));

  return sessions.map((session) => {
    const event_speakers = session.speaker_ids
      .map((speakerId) => {
        const speaker = speakersById.get(speakerId);
        return speaker
          ? {
              ...speaker,
            }
          : null;
      })
      .filter(Boolean);

    return {
      ...session,
      speakers: event_speakers.map((speaker) => speaker.initials),
      event_speakers,
      speaker_ids: undefined,
    };
  });
}

function getSpeakersList() {
  return eventSpeakers.map((speaker) => ({
    ...speaker,
    sessions: getSpeakerSessions(speaker.id),
  }));
}

function getAttendeesList() {
  return attendeesList;
}

function getSpeakerSessions(speakerId) {
  return sessions
    .filter((session) => session.speaker_ids.includes(speakerId))
    .map((session) => ({
      id: session.id,
      title: session.title,
      track: session.track,
      time: session.time,
      date: session.date,
      place: session.place,
    }));
}

